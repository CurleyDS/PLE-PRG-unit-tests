/**
 * Returns the sum of a + b
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
export function sum(a, b) {
    return a + b;
}

/**
 * Returns the substraction of a - b - c
 * @param {number} a 
 * @param {number} b 
 * @param {number} c 
 * @returns {number}
 */
export function subtract(a, b, c = 0) {
    return a - b - c;
}